/**
 * Obsidian Integration for Knowledge Trading Agents Simulation
 * 
 * Auto-saves simulation events to the Obsidian vault:
 *   C:\Users\staff\anthropicFun\knowledge-trading-agents\_archive\Hermes Knowledge bank
 * 
 * Events captured:
 *   - Simulation start/stop
 *   - Goal completed/failed
 *   - Agent strategy switches
 *   - Deadlock resolutions
 *   - Marketplace activity (aggregate)
 *   - Agent net worth milestones
 * 
 * Usage: Include in app.js, initialize with obsidianIntegration.init(sim, vaultPath)
 */

class ObsidianIntegration {
  constructor() {
    this.vaultPath = null;
    this.sim = null;
    this.eventLog = [];
    this.sessionId = this._generateSessionId();
    this.sessionStartTime = new Date();
  }

  /**
   * Initialize integration with simulation instance and vault path
   */
  init(simulation, vaultPath) {
    this.sim = simulation;
    this.vaultPath = vaultPath;
    
    // Hook into simulation events
    this._hookSimulationEvents();
    
    // Create session note
    this._createSessionNote();
    
    console.log(`✓ Obsidian integration initialized (vault: ${vaultPath})`);
  }

  /**
   * Hook into simulation event emitter
   */
  _hookSimulationEvents() {
    const originalEmit = this.sim._emit.bind(this.sim);
    
    this.sim._emit = (type, message, color) => {
      originalEmit(type, message, color);
      this._logEvent(type, message, color);
    };
  }

  /**
   * Generate unique session ID
   */
  _generateSessionId() {
    const now = new Date();
    return `session-${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}-${String(now.getHours()).padStart(2, '0')}-${String(now.getMinutes()).padStart(2, '0')}-${String(now.getSeconds()).padStart(2, '0')}`;
  }

  /**
   * Log event for later batch save
   */
  _logEvent(type, message, color) {
    this.eventLog.push({
      round: this.sim.round,
      type,
      message,
      color,
      timestamp: new Date().toISOString(),
    });
  }

  /**
   * Create session note (runs at init)
   */
  _createSessionNote() {
    const sessionContent = `# Simulation Session: ${this.sessionId}

**Start time**: ${this.sessionStartTime.toISOString()}
**Seed**: ${this.sim.seed}

## Objectives

Track simulation progression, agent behaviors, and emergent patterns.

## Milestones

| Round | Event | Details |
|-------|-------|---------|
| — | Session start | Awaiting first tick |

## Summary

- Total rounds: [pending]
- Goals completed: [pending]
- Final leader: [pending]
- Avg completion time: [pending]

---

*Auto-generated by Obsidian Integration*
`;

    this._writeFile(`_Sessions/${this.sessionId}.md`, sessionContent);
  }

  /**
   * Save current simulation state to session note (call periodically)
   */
  updateSessionNote() {
    const leader = this.sim.leader();
    const avgGoalDuration = this.sim.goalHistory.length > 0
      ? Math.round(
          this.sim.goalHistory.reduce((s, g) => s + (g.endRound - g.startRound), 0) /
          this.sim.goalHistory.length
        )
      : 0;

    const milestonesTable = this.eventLog
      .filter(e => ['council', 'task'].includes(e.type))
      .slice(-20)
      .map(e => `| ${e.round} | ${e.type} | ${e.message} |`)
      .join('\n');

    const agentSummary = this.sim.agents
      .map(a => `- **${a.name}** (${a.strategy}): ${this.sim.netWorth(a)} net, ${Math.round(this.sim.skillAvg(a))} avg skill`)
      .join('\n');

    const content = `# Simulation Session: ${this.sessionId}

**Start time**: ${this.sessionStartTime.toISOString()}
**Seed**: ${this.sim.seed}

## Current Status

**Round**: ${this.sim.round}
**Goals completed**: ${this.sim.goalHistory.length}
**Current leader**: [[${leader.name}]] (${this.sim.netWorth(leader)} net worth)

## Agent States

${agentSummary}

## Milestones

| Round | Event | Details |
|-------|-------|---------|
${milestonesTable}

## Statistics

| Metric | Value |
|--------|-------|
| Total rounds | ${this.sim.round} |
| Goals completed | ${this.sim.goalHistory.length} |
| Tasks completed | ${this.sim.totals.tasksDone} |
| Tasks failed | ${this.sim.totals.tasksFailed} |
| Trades executed | ${this.sim.totals.trades} |
| Credits traded | ${this.sim.totals.creditsTraded} |
| Avg goal duration | ${avgGoalDuration} rounds |

## Wikilinks

- [[Simulation Architecture]]
- [[Agent Strategies]]
- [[Council Voting]]
- [[Simulation Runs]]

---

*Last updated*: ${new Date().toISOString()}
*Auto-generated by Obsidian Integration*
`;

    this._writeFile(`_Sessions/${this.sessionId}.md`, content);
  }

  /**
   * Save goal completion event
   */
  saveGoalCompletion(goal) {
    const agents = this.sim.agents.map(a => a.name).join(', ');
    const goalType = goal.type === 'skill' 
      ? `Skill: +${Math.round(goal.progress)} avg in ${goal.domain}`
      : goal.type === 'tasks'
      ? `Tasks: ${goal.done}/${goal.target} completed in ${goal.domain}`
      : `Knowledge: +${goal.progress} quality in ${goal.domain}`;

    const content = `# Goal: ${goal.title}

**Completed round**: ${goal.endRound}
**Duration**: ${goal.endRound - goal.startRound} rounds
**Status**: ${goal.status === 'done' ? '✓ Shipped' : '✗ Draft (missed)'}
**Born from**: ${goal.bornFrom}

## Objective

Type: ${goal.type}
Domain: ${goal.domain}
Target: ${goal.target}
Progress: ${goalType}

## Deliverable Value

Base value: ${goal.status === 'done' ? '40' : '20'} credits
Skill contribution: [calculated]
Knowledge contribution: [calculated]
Total value: [calculated]

## Contributors

${agents}

## Agents' Performance This Goal

| Agent | Strategy | Tasks | Skills | Knowledge Contributed |
|-------|----------|-------|--------|----------------------|
${this.sim.agents.map(a => `| ${a.name} | ${a.strategy} | — | — | — |`).join('\n')}

---

*Recorded*: ${new Date().toISOString()}
*Related goals*: [[${goal.title}]]
*Session*: [[${this.sessionId}]]
`;

    this._writeFile(`Simulation Runs/${this.sessionId}-goal-${goal.id}.md`, content);
  }

  /**
   * Save agent strategy switch
   */
  saveStrategySwitch(agent, oldStrategy, newStrategy, reason) {
    const content = `# Strategy Switch: ${agent.name}

**Round**: ${this.sim.round}
**From**: ${oldStrategy}
**To**: ${newStrategy}
**Reason**: ${reason}

## Context

- Success rate: ${Math.round(this.sim.successRate(agent) * 100)}%
- Skill spread: ${Math.round(this.sim.topDomain(agent) - this.sim.weakDomain(agent))}
- Credits: ${agent.credits}
- Wealth rank: ${[...this.sim.agents].sort((x, y) => this.sim.netWorth(y) - this.sim.netWorth(x)).indexOf(agent) + 1}

## New Focus

*${newStrategy}* agent will pursue:
${this._strategyFocus(newStrategy)}

---

*Recorded*: ${new Date().toISOString()}
*Session*: [[${this.sessionId}]]
*Agent*: [[${agent.name}]]
`;

    this._writeFile(`Agent Behavior Patterns/${this.sessionId}-${agent.name}-switch.md`, content);
  }

  /**
   * Save deadlock resolution
   */
  saveDeadlockResolution(operatorChoice, candidates, previousVotes) {
    const deadlockContent = `# Deadlock Resolution: Round ${this.sim.round}

**Session**: [[${this.sessionId}]]
**Operator choice**: ${operatorChoice.title}

## Context

The council deadlocked on the topic choice. Vote distribution:

${candidates.map((c, i) => `- **${c.title}**: ${Math.round((previousVotes[c.id] || 0) / Object.values(previousVotes).reduce((s, v) => s + v, 0) * 100)}%`).join('\n')}

Close enough that no consensus emerged.

## Operator Decision

**Chosen**: ${operatorChoice.title}
**Rationale**: [operator input]

## Outcome

Agents will now pursue: ${operatorChoice.goalText}

---

*Recorded*: ${new Date().toISOString()}
*Related*: [[Operator Decision Impact]]
*Session*: [[${this.sessionId}]]
`;

    this._writeFile(`Operator Decision Impact/${this.sessionId}-deadlock.md`, deadlockContent);
  }

  /**
   * Strategy focus text
   */
  _strategyFocus(strategy) {
    const focuses = {
      Specialist: '- Exploit strong domains (1.6× appetite)\n- Refuse to buy knowledge\n- Sell at premium (0.60)',
      Trader: '- Target knowledge-yielding tasks (1.5×)\n- Buy weakest domains to resell\n- Sell at volume price (0.34)',
      Explorer: '- Pursue weak-domain tasks (1.5×)\n- Aggressively buy to fill gaps (70% budget)\n- Learn foundational skills',
      Imitator: '- Follow leader\'s strong domain (1.4×)\n- Buy what leader is strong in\n- Easily persuaded in votes',
      Generalist: '- Neutral on all tasks (1.0×)\n- Buy weakest to balance\n- Vote for collective good (60%)',
    };
    return focuses[strategy] || 'Unknown strategy';
  }

  /**
   * Write file to vault
   * (In real implementation, would use Obsidian API or file system)
   */
  _writeFile(relativePath, content) {
    const fullPath = `${this.vaultPath}/${relativePath}`;
    
    // For now, log to console (would write to FS in real implementation)
    console.log(`📝 Would save to: ${fullPath}`);
    
    // In production, use:
    // fs.writeFileSync(fullPath, content, 'utf-8');
  }

  /**
   * Export session summary as markdown
   */
  exportSessionSummary() {
    const leader = this.sim.leader();
    const avgNet = Math.round(
      this.sim.agents.reduce((s, a) => s + this.sim.netWorth(a), 0) / this.sim.agents.length
    );

    return `# Session Summary: ${this.sessionId}

**Duration**: ${Math.round((new Date() - this.sessionStartTime) / 1000)}s real-time
**Rounds simulated**: ${this.sim.round}
**Seed**: ${this.sim.seed}

## Final Rankings

${this.sim.agents
  .map((a, i) => `${i + 1}. **${a.name}** (${a.strategy}): ${this.sim.netWorth(a)} net worth`)
  .join('\n')}

## Aggregate Stats

- **Total goals completed**: ${this.sim.goalHistory.length}
- **Tasks completed**: ${this.sim.totals.tasksDone}
- **Tasks failed**: ${this.sim.totals.tasksFailed}
- **Knowledge trades**: ${this.sim.totals.trades}
- **Credits in circulation**: ${this.sim.totals.creditsTraded}

## Notable Events

${this.eventLog
  .filter(e => e.type === 'council')
  .slice(-5)
  .map(e => `- Round ${e.round}: ${e.message}`)
  .join('\n')}

---

*Exported*: ${new Date().toISOString()}
`;
  }
}

// Create global instance
const obsidianIntegration = new ObsidianIntegration();
